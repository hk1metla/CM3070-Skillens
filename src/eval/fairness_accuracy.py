"""
Fairness accuracy-by-demographic-group (Contribution 2).

Computes accuracy metrics (NDCG@10, Precision@10, Recall@10) per demographic
group for the Hybrid model using the same evaluation protocol as
src/eval/comprehensive_eval.py:
  - relevant items = test items not seen in training for that user
  - query/goal built from training history only (no test leakage)
  - recs generated by the same recommend() calls

Outputs:
  results/final/fairness_accuracy.csv
"""

from __future__ import annotations

import argparse
import os
from typing import Dict, List, Tuple

import numpy as np
import pandas as pd

from src.eval.metrics import ndcg_at_k, precision_at_k, recall_at_k
from src.eval.statistical import bootstrap_confidence_interval
from src.models.hybrid import HybridRecommender
from src.data.load_demographics import get_demographic_groups, load_demographics


BASE_DIR = os.path.abspath(os.path.join(os.path.dirname(__file__), "..", ".."))
DATA_DIR = os.path.join(BASE_DIR, "data", "processed")


def _load_oulad_items() -> pd.DataFrame:
    items = pd.read_csv(os.path.join(DATA_DIR, "items.csv"), dtype={"item_id": str})
    oulad_items = items[items["item_id"].str.startswith("oulad_", na=False)].copy()
    if len(oulad_items) == 0:
        raise RuntimeError("No OULAD items found in data/processed/items.csv")
    return oulad_items


def _load_oulad_split(name: str) -> pd.DataFrame:
    split_df = pd.read_csv(
        os.path.join(DATA_DIR, f"{name}.csv"),
        dtype={"user_id": str, "item_id": str},
    )
    oulad_split = split_df[split_df["item_id"].str.startswith("oulad_", na=False)].copy()
    if len(oulad_split) == 0:
        raise RuntimeError(f"No OULAD interactions found in data/processed/{name}.csv")
    return oulad_split


def _pretty_label(demo_category: str, raw_group: str) -> str:
    # Keep labels aligned to what the report currently shows.
    if demo_category == "gender":
        return {"M": "Male", "F": "Female"}.get(raw_group, raw_group)
    if demo_category == "age_band":
        # In OULAD, age_band often uses '55<='.
        return {"55<=": "55+", "0-35": "0-35", "35-55": "35-55"}.get(raw_group, raw_group)
    if demo_category == "highest_education":
        mapping = {
            "HE Qualification": "HE Qualification",
            "A Level or Equivalent": "A Level",
            "Lower Than A Level": "Lower than A Level",
        }
        return mapping.get(raw_group, raw_group)
    return raw_group


def _ref_raw_group(demo_category: str) -> str:
    # Reference groups to match the current Table 12 layout (baseline rows).
    if demo_category == "gender":
        return "M"
    if demo_category == "age_band":
        return "0-35"
    if demo_category == "highest_education":
        return "HE Qualification"
    raise ValueError(f"Unexpected demo_category for reference: {demo_category}")


def _sample_users(user_ids: List[str], max_users: int, seed: int) -> List[str]:
    if len(user_ids) <= max_users:
        return user_ids
    rng = np.random.default_rng(seed)
    return rng.choice(user_ids, size=max_users, replace=False).tolist()


def _evaluate_hybrid_for_users(
    hybrid: HybridRecommender,
    items_lookup: pd.DataFrame,
    train_items_by_user: Dict[str, List[str]],
    test_items_by_user: Dict[str, List[str]],
    user_ids: List[str],
    k: int,
) -> Dict[str, List[float]]:
    """
    Evaluate NDCG/Precision/Recall for a set of users.

    Returns dict with keys: precision, recall, ndcg (each list aligned to evaluated users).
    """
    precision_vals: List[float] = []
    recall_vals: List[float] = []
    ndcg_vals: List[float] = []

    for user_id in user_ids:
        user_train_items = train_items_by_user.get(user_id, [])
        if not user_train_items:
            continue

        user_test_items = test_items_by_user.get(user_id, [])
        if not user_test_items:
            continue

        train_items_set = set(user_train_items)
        relevant_set = list(set(user_test_items) - train_items_set)
        if not relevant_set:
            continue

        # Build goal query from TRAIN history only (no test leakage).
        goal_text = " ".join(
            items_lookup.loc[item_id, "title"]
            for item_id in user_train_items[:3]
            if item_id in items_lookup.index
        )
        if not goal_text.strip():
            continue

        # Minimal user-interactions frame for ItemKNN recommend() call.
        # (ItemKNN only reads columns user_id and item_id.)
        user_train_df = pd.DataFrame(
            {"user_id": [user_id] * len(user_train_items), "item_id": user_train_items}
        )

        recs_df = hybrid.recommend(
            goal=goal_text,
            user_id=user_id,
            user_interactions=user_train_df,
            k=k,
        )
        recs = recs_df["item_id"].tolist()
        if not recs:
            continue

        precision_vals.append(precision_at_k(recs, relevant_set, k))
        recall_vals.append(recall_at_k(recs, relevant_set, k))
        ndcg_vals.append(ndcg_at_k(recs, relevant_set, k))

    return {
        "precision": precision_vals,
        "recall": recall_vals,
        "ndcg": ndcg_vals,
    }


def run_fairness_accuracy(
    out_dir: str,
    k: int = 10,
    max_users_per_group: int = 500,
    seed: int = 42,
    n_bootstrap: int = 500,
) -> pd.DataFrame:
    os.makedirs(out_dir, exist_ok=True)

    items = _load_oulad_items()
    train_df = _load_oulad_split("train")
    test_df = _load_oulad_split("test")

    demographics = load_demographics()
    if demographics.empty:
        raise RuntimeError("No demographics found in anonymised OULAD data.")

    # Only evaluate users that appear in interactions for both splits.
    train_users = set(train_df["user_id"].astype(str).unique())
    test_users = set(test_df["user_id"].astype(str).unique())
    demographics = demographics[demographics["user_id"].astype(str).isin(train_users)]

    if demographics.empty:
        raise RuntimeError("Demographics dataset is empty after filtering to train users.")

    # Define the exact demographic categories we report in the paper (matches current Table 12).
    reported_categories = ["gender", "age_band", "highest_education"]
    demo_groups = get_demographic_groups(demographics)

    # Convert report categories to the internal group keys format produced by get_demographic_groups.
    # get_demographic_groups produces keys like: gender_M, age_55<=, education_he_qualification, etc.
    # We'll use raw column values instead of the derived keys.
    def users_for_category_group(category: str, raw_value: str) -> List[str]:
        mask = demographics[category] == raw_value
        return demographics.loc[mask, "user_id"].astype(str).tolist()

    # Select which raw groups to include (so Table 12 remains consistent).
    groups_to_include: Dict[str, List[str]] = {
        "gender": ["M", "F"],
        "age_band": ["0-35", "35-55", "55<="],
        "highest_education": ["HE Qualification", "A Level or Equivalent", "Lower Than A Level"],
    }

    # Build union of sampled users across all included categories/groups.
    all_sampled_users: List[str] = []
    sampled_users_by_category: Dict[Tuple[str, str], List[str]] = {}

    for category in reported_categories:
        for raw_group in groups_to_include[category]:
            users = users_for_category_group(category, raw_group)
            users = [u for u in users if u in test_users]
            users = _sample_users(users, max_users_per_group, seed=seed)
            sampled_users_by_category[(category, raw_group)] = users
            all_sampled_users.extend(users)

    all_sampled_users = sorted(set(all_sampled_users))
    if not all_sampled_users:
        raise RuntimeError("No sampled users found for fairness accuracy.")

    # Filter train/test interactions to only sampled users for speed.
    train_small = train_df[train_df["user_id"].astype(str).isin(all_sampled_users)].copy()
    test_small = test_df[test_df["user_id"].astype(str).isin(all_sampled_users)].copy()

    train_items_by_user = (
        train_small.groupby("user_id")["item_id"].apply(list).to_dict()  # type: ignore[no-untyped-call]
    )
    test_items_by_user = (
        test_small.groupby("user_id")["item_id"].apply(list).to_dict()  # type: ignore[no-untyped-call]
    )

    # Fit Hybrid model on full TRAIN interactions (consistent with comprehensive_eval).
    # We still evaluate only a sampled subset of users for speed, but the CF
    # similarity model should be trained on the complete training split so
    # Table 12 is on the same candidate-set definition as Table~10.
    hybrid = HybridRecommender(w_content=0.6, w_cf=0.4)
    hybrid.fit(items, train_df[["user_id", "item_id"]].copy())

    items_lookup = items.set_index("item_id")

    # First pass: compute per-group mean ndcg values, to allow reference-gap calculation.
    results: List[Dict[str, object]] = []

    ndcg_means_by_category: Dict[str, float] = {}
    ndcg_mean_by_group: Dict[Tuple[str, str], float] = {}

    for category in reported_categories:
        ref_raw = _ref_raw_group(category)

        # Evaluate all groups for this category to compute reference values.
        # We compute ndcg values list, then CI + mean.
        for raw_group in groups_to_include[category]:
            user_ids = sampled_users_by_category[(category, raw_group)]
            vals = _evaluate_hybrid_for_users(
                hybrid=hybrid,
                items_lookup=items_lookup,
                train_items_by_user=train_items_by_user,
                test_items_by_user=test_items_by_user,
                user_ids=user_ids,
                k=k,
            )

            ndcg_values = vals["ndcg"]
            precision_values = vals["precision"]
            recall_values = vals["recall"]

            if not ndcg_values:
                # Skip groups with no valid evaluation users.
                continue

            ndcg_mean, ndcg_ci_lower, ndcg_ci_upper = bootstrap_confidence_interval(
                ndcg_values,
                confidence=0.95,
                n_bootstrap=n_bootstrap,
                random_seed=seed,
            )
            precision_mean, precision_ci_lower, precision_ci_upper = bootstrap_confidence_interval(
                precision_values,
                confidence=0.95,
                n_bootstrap=n_bootstrap,
                random_seed=seed,
            )
            recall_mean, recall_ci_lower, recall_ci_upper = bootstrap_confidence_interval(
                recall_values,
                confidence=0.95,
                n_bootstrap=n_bootstrap,
                random_seed=seed,
            )

            ndcg_mean_by_group[(category, raw_group)] = ndcg_mean

            results.append(
                {
                    "demographic_category": category,
                    "group": _pretty_label(category, raw_group),
                    "raw_group": raw_group,
                    "model": "hybrid",
                    "n_users": len(ndcg_values),
                    "precision_mean": precision_mean,
                    "precision_ci_lower": precision_ci_lower,
                    "precision_ci_upper": precision_ci_upper,
                    "recall_mean": recall_mean,
                    "recall_ci_lower": recall_ci_lower,
                    "recall_ci_upper": recall_ci_upper,
                    "ndcg_mean": ndcg_mean,
                    "ndcg_ci_lower": ndcg_ci_lower,
                    "ndcg_ci_upper": ndcg_ci_upper,
                }
            )

    # Second pass: add gap vs reference group for each category.
    for row in results:
        category = row["demographic_category"]
        raw_group = row["raw_group"]
        ref_raw = _ref_raw_group(str(category))
        ref_mean = ndcg_mean_by_group.get((category, ref_raw))
        if ref_mean is None or ref_mean == 0:
            row["ndcg_gap_percent"] = np.nan
        else:
            row["ndcg_gap_percent"] = (float(row["ndcg_mean"]) - ref_mean) / ref_mean * 100.0

    df = pd.DataFrame(results)
    df = df.sort_values(["demographic_category", "raw_group"]).reset_index(drop=True)

    out_path = os.path.join(out_dir, "fairness_accuracy.csv")
    df.to_csv(out_path, index=False)
    print(f"Fairness accuracy saved to: {out_path}")
    return df


def main():
    parser = argparse.ArgumentParser(description="Compute fairness accuracy by demographic group (Hybrid model).")
    parser.add_argument("--out", default=os.path.join(BASE_DIR, "results", "final"), help="Output directory.")
    parser.add_argument("--k", type=int, default=10, help="Top-K for ranking metrics.")
    parser.add_argument("--max-users-per-group", type=int, default=500)
    parser.add_argument("--seed", type=int, default=42)
    parser.add_argument("--n-bootstrap", type=int, default=500)
    args = parser.parse_args()

    run_fairness_accuracy(
        out_dir=args.out,
        k=args.k,
        max_users_per_group=args.max_users_per_group,
        seed=args.seed,
        n_bootstrap=args.n_bootstrap,
    )


if __name__ == "__main__":
    main()

